pub fn glyphFor(c: u8) *const Glyph {
    if (c < 32 or c > 126) return &glyphs['?' - 32];
    return &glyphs[c - 32];
}

test "font sanity" {
    // 'T': top row solid, stem down the middle
    const t = glyphFor('T');
    try std.testing.expectEqual(@as(u8, 0b11111), t[0]);
    try std.testing.expectEqual(@as(u8, 0b00100), t[6]);
}
