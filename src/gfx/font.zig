//! Tiny 5x7 bitmap font (ASCII 32..126), parsed at comptime from glyph art.
//! Rendered with integer scaling; good enough for labels and annotations.

const std = @import("std");

pub const glyph_w = 5;
pub const glyph_h = 7;

/// Each glyph is 7 rows of 5 bits, MSB = leftmost column.
pub const Glyph = [glyph_h]u8;

fn parseGlyph(comptime art: *const [glyph_h][glyph_w]u8) Glyph {
    var g: Glyph = undefined;
    for (art, 0..) |row, y| {
        var bits: u8 = 0;
        for (row, 0..) |c, x| {
            if (c == '#') bits |= @as(u8, 0x10) >> @intCast(x);
        }
        g[y] = bits;
    }
    return g;
}

fn G(comptime rows: [glyph_h]*const [glyph_w]u8) Glyph {
    comptime {
        @setEvalBranchQuota(200_000);
        var art: [glyph_h][glyph_w]u8 = undefined;
        for (rows, 0..) |r, i| art[i] = r.*;
        return parseGlyph(&art);
    }
}

